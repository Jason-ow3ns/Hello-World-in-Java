package com.company;

public class operatorAbbreviations {

    public static void main(String[] args) {

        // +1 abbreviation
        int result = 1;
        result++;
        System.out.println(" 1 + 1 = " + result);

        // -1 abbreviation
        result--;
        System.out.println("2 - 1 = " + result);

        // +x abbreviation
        result += 3;
        System.out.println("1 + 3 = " + result);

        // -x abbreviation
        result -= 2;
        System.out.println("4 - 2 = " + result);

        // *x abbreviation
        result *= 4;
        System.out.println("2 * 4 = " + result);

        // /x abbreviation
        result /= 2;
        System.out.println("8 / 2 = " + result);

        // %x abbreviation
        result %= 3;
        System.out.println("4 % 2 = " + result);

    }
}
